#include "3-calc.h"
/**
 * op_add - sum
 * @a: 1 num.
 * @b: 2 num
 * Return: a + b
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - sub
 * @a: 1 num
 * @b: 2 num
 * Return: a - b
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - mul
 * @a: 1 num
 * @b: 2 num
 * Return: a * b
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - div
 * @a: 1 num
 * @b: 2 num
 * Return: a / b
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - mod
 * @a: 1 num
 * @b: 2 num
 * Return: a %  b
 */
int op_mod(int a, int b)
{
	return (a % b);
}
